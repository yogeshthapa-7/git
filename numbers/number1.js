// Simple Numbers Example - Basic Math Operations

let a = 10;
let b = 3;

console.log("a =", a);
console.log("b =", b);
console.log("a + b =", a + b);        // Addition
console.log("a - b =", a - b);        // Subtraction
console.log("a * b =", a * b);        // Multiplication
console.log("a / b =", a / b);        // Division (with decimal)
console.log("a % b =", a % b);        // Remainder
console.log("a ** 2 =", a ** 2);      // a squared

// Round numbers
let price = 19.99;
console.log("\nPrice:", price);
console.log("Rounded up:", Math.ceil(price));
console.log("Rounded down:", Math.floor(price));
console.log("Rounded normally:", Math.round(price));